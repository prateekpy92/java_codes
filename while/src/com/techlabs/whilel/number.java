package com.techlabs.whilel;

public class number {

	public static void main(String[] args) {
		
		        int i = 1;
		        while (i <= 10) {
		            int j = 1;
		            while (j <= 10) {
		                System.out.printf("%2d ",  (i * j));
		                j++;
		            }
		            System.out.println();
		            i++;
		        }
		    }
		

	}


